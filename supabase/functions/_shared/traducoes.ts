// Função auxiliar para traduzir texto
export function traduzir(
  texto: string | null | undefined,
  dicionario: Record<string, string>
): string {
  if (!texto) return '';
  
  const textoLimpo = texto.trim();
  
  // Busca exata (case-insensitive)
  const chaveExata = Object.keys(dicionario).find(
    key => key.toLowerCase() === textoLimpo.toLowerCase()
  );
  
  if (chaveExata) {
    return dicionario[chaveExata];
  }
  
  // Se não encontrar, retorna o original
  return textoLimpo;
}

// Funções específicas por tipo
export function traduzirCategoria(categoria: string | null | undefined): string {
  return traduzir(categoria, CATEGORIAS_PT);
}

export function traduzirArea(area: string | null | undefined): string {
  return traduzir(area, AREAS_PT);
}

export function traduzirIngrediente(ingrediente: string | null | undefined): string {
  return traduzir(ingrediente, INGREDIENTES_PT);
}

export function traduzirMedida(medida: string | null | undefined): string {
  if (!medida) return '';
  
  const medidaLimpa = medida.trim().toLowerCase();
  
  // Traduzir cada palavra da medida
  const palavras = medidaLimpa.split(' ');
  const palavrasTraduzidas = palavras.map(palavra => {
    const traducao = MEDIDAS_PT[palavra];
    return traducao || palavra;
  });
  
  return palavrasTraduzidas.join(' ');
}
